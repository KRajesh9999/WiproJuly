function sum(x : number, y : number) : number 
{
    return x + y;
}

function sub(x : number, y : number) : number 
{
    return x - y;
}

function mul(x : number, y : number) : number 
{
    return x * y;
}

console.log("Sum is  "+sum(12,8));
console.log("Subtraction is  "+sub(12,8));
console.log("Multiplication is  "+mul(12,8));